/*
Faça um Programa que peça dois números e imprima a soma.
*/

#include <iostream>
using namespace std;

int main(){
    short int a, b;
    cout << "Informe o primeiro número: " << "\n";
    cin >> a;
    cout << "Informe o segundo número: " << "\n";
    cin >> b;

    cout << "A soma é: " << a + b;

    return 0;

}